public class PlayerCardPrinter {
    public static void main(String[] args) {

        System.out.print("#################################\n");
        System.out.println("#         Baldur's Gate         #"); 
        System.out.println("#################################");
        System.out.println("#                               #");   
        System.out.println("#\tName:\tHalsin\t\t#");
        System.out.println("#\tRace:\tWood Elf\t#");
        System.out.println("#\tClass:\tDruid\t\t#");
        System.out.println("#\tLevel:\t12\t\t#");
        System.out.println("#                               #");
        System.out.println("############# Quote #############");
        System.out.println("#                               #");
        System.out.print("#     \"Nature's gifts are\t# \n#    bountiful, but her wrath\t# \n#\tis just as fierce.\"\t#\n");
        System.out.println("#                               #");   
        System.out.println("#################################");       
    }
}

